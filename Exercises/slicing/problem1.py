# Given this list:

# colors = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]
# Using list slicing, print:

# The first 3 elements
# The last 3 elements
# Everything except the first and last element

colors = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]
print(len(colors))

print(colors[:3])
print(colors[4:])
print(colors[1:6])
print(colors[::-1])